"""Manifest parsing and resolution.

Reads `.agents/manifest.yaml` and `.agents/manifest.lock.yaml`.
Validates schema. Resolves version pins (^, ~, exact, latest) against
registry-published versions.

Phase 0 baseline. Pin resolution is simplified — full semver matching
will arrive in subsequent versions.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

import yaml


MANIFEST_PATH = ".agents/manifest.yaml"
LOCK_PATH = ".agents/manifest.lock.yaml"


@dataclass
class Manifest:
    budai_version: str
    included_roles: list[str] = field(default_factory=list)
    included_skills: dict[str, str] = field(default_factory=dict)
    included_workflows: dict[str, str] = field(default_factory=dict)
    included_runners: dict[str, str] = field(default_factory=dict)
    overrides: dict[str, str] = field(default_factory=dict)
    local_only: dict[str, list[str]] = field(default_factory=dict)
    human_gates: list[str] = field(default_factory=list)
    defaults: dict[str, Any] = field(default_factory=dict)
    runtime_backend: dict[str, Any] | None = None


def load_manifest(repo_root: Path) -> Manifest:
    """Read and parse `.agents/manifest.yaml`."""
    manifest_file = repo_root / MANIFEST_PATH
    if not manifest_file.exists():
        raise FileNotFoundError(f"Manifest not found at {manifest_file}")

    with manifest_file.open() as f:
        data = yaml.safe_load(f) or {}

    included = data.get("included", {}) or {}

    return Manifest(
        budai_version=data.get("budai-version", "unknown"),
        included_roles=_normalize_role_list(included.get("roles", [])),
        included_skills=_normalize_pin_list(included.get("skills", [])),
        included_workflows=_normalize_pin_list(included.get("workflows", [])),
        included_runners=_normalize_pin_list(included.get("runners", [])),
        overrides=data.get("overrides", {}) or {},
        local_only=data.get("local-only", {}) or {},
        human_gates=data.get("human-gates", []),
        defaults=data.get("defaults", {}) or {},
        runtime_backend=data.get("runtime-backend"),
    )


def _normalize_role_list(value: Any) -> list[str]:
    """Roles are listed as plain strings."""
    if not value:
        return []
    return [str(item) for item in value]


def _normalize_pin_list(value: Any) -> dict[str, str]:
    """Convert a list like `[name, name@version, {name: pin}]` to {name: pin}.

    Manifest items in the YAML can be either:
    - a plain name (uses default pin)
    - a `name: pin` mapping
    """
    result: dict[str, str] = {}
    if not value:
        return result

    for item in value:
        if isinstance(item, str):
            result[item] = "*"  # any version
        elif isinstance(item, dict):
            for name, pin in item.items():
                result[str(name)] = str(pin)
        else:
            raise ValueError(f"Cannot parse manifest item: {item!r}")

    return result


def load_lockfile(repo_root: Path) -> dict[str, Any]:
    """Read `.agents/manifest.lock.yaml` if present; return empty dict otherwise."""
    lock_file = repo_root / LOCK_PATH
    if not lock_file.exists():
        return {}

    with lock_file.open() as f:
        return yaml.safe_load(f) or {}


def write_lockfile(repo_root: Path, lock_data: dict[str, Any]) -> None:
    """Write resolved versions to the lockfile."""
    lock_file = repo_root / LOCK_PATH
    lock_file.parent.mkdir(parents=True, exist_ok=True)

    with lock_file.open("w") as f:
        f.write("# Generated by librarian sync; do not edit by hand.\n")
        yaml.safe_dump(lock_data, f, default_flow_style=False, sort_keys=False)


def find_repo_root(start: Path | None = None) -> Path:
    """Walk up to find the repo root (presence of .agents/ or .git/)."""
    current = (start or Path.cwd()).resolve()
    for candidate in [current, *current.parents]:
        if (candidate / ".agents").exists() or (candidate / ".git").exists():
            return candidate
    raise FileNotFoundError("Not inside a budai repo (no .agents/ or .git/ found)")
